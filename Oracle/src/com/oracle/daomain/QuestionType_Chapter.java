package com.oracle.daomain;

public class QuestionType_Chapter {
private String ChapterID;//章节号
private String ChapterName;//章节名
private String QuestionTypePronoun;//题型号
private String CourseID;//课程ID
private String ProblemNumber;//习题数
public String getChapterID() {
	return ChapterID;
}
public void setChapterID(String chapterID) {
	ChapterID = chapterID;
}
public String getChapterName() {
	return ChapterName;
}
public void setChapterName(String chapterName) {
	ChapterName = chapterName;
}
public String getQuestionTypePronoun() {
	return QuestionTypePronoun;
}
public void setQuestionTypePronoun(String questionTypePronoun) {
	QuestionTypePronoun = questionTypePronoun;
}
public String getCourseID() {
	return CourseID;
}
public void setCourseID(String courseID) {
	CourseID = courseID;
}
public String getProblemNumber() {
	return ProblemNumber;
}
public void setProblemNumber(String problemNumber) {
	ProblemNumber = problemNumber;
}

}
